package com.xml.standards;

public class MethodProceed {
	private String methodProceed;
	private String unit;
	private String dilution;
	private String highlight;
	private String quantity;
	private String addition;
	private String search;
	private String medium;
	
	public String getUnit() {
		return unit;
	}
	public void setUnit(String unit) {
		this.unit = unit;
	}
	public String getDilution() {
		return dilution;
	}
	public void setDilution(String dilution) {
		this.dilution = dilution;
	}
	public String getHighlight() {
		return highlight;
	}
	public void setHighlight(String highlight) {
		this.highlight = highlight;
	}
	public String getQuantity() {
		return quantity;
	}
	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}
	public String getAddition() {
		return addition;
	}
	public void setAddition(String addition) {
		this.addition = addition;
	}
	public String getSearch() {
		return search;
	}
	public void setSearch(String search) {
		this.search = search;
	}
	public String getMedium() {
		return medium;
	}
	public void setMedium(String medium) {
		this.medium = medium;
	}
	public String getMethodProceed() {
		return methodProceed;
	}
	public void setMethodProceed(String methodProceed) {
		this.methodProceed = methodProceed;
	}

}
